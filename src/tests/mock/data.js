export default function transformData() {
  return stockdata.c.map((item, index) => ({
    close: Number(item).toFixed(2),
    open: Number(stockdata.o[index]).toFixed(2),
    timestamp: new Date(stockdata.t[index] * 1000).toUTCString(),
  }));
}

export function numFormat(num, digits) {
  var si = [
    { value: 1, symbol: "" },
    { value: 1e3, symbol: "k" },
    { value: 1e6, symbol: "M" },
    { value: 1e9, symbol: "B" },
  ];
  var rx = /\.0+$|(\.[0-9]*[1-9])0+$/;
  var i;
  for (i = si.length - 1; i > 0; i--) {
    if (num >= si[i].value) {
      break;
    }
  }
  return (num / si[i].value).toFixed(digits).replace(rx, "$1") + si[i].symbol;
}
